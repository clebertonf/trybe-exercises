
//Exercicio 01 operadores aritimeticos refatorados com uso de funçoes

adicao(5,5);
subtracao(10,2);
multiplicacao(5,3);
divisao(20,3);
modulo(10,3);

//Adição
function adicao(a,b){
    return console.log(a + b);
}

//subtração
function subtracao(a,b){
    return console.log(a - b);
}

//Multiplicaçao
function multiplicacao(a,b){
    return console.log(a * b);
}

//Divisão
function divisao(a,b){
    return console.log(a / b);
}

//Modulo
function modulo(a,b){
    return console.log(a % b)
}
